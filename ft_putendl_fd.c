#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	int	x;

	x = 0;
	if (!s)
		return ;
	while (s[x] != '\0')
	{
		write(fd, &((char *)s)[x], 1);
		x++;
	}
	write(fd, "\n", 1);
}
